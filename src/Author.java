public class Author {

    private String name;
    private String lastName;
    private int age;

    public Author(String name, String lastName, int age) {
        this.name = name;
        this.lastName = lastName;
        this.age = age;
    }

    public String getName() {
        return this.name;
    }

    public String getLastNameName() {
        return this.lastName;
    }

    public int getage() {
        return this.age;
    }
}
